import {
	useCallback,
	useEffect,
	useId,
	useMemo,
	useRef,
	useState,
} from "react";
import { summarizeInvitationContent } from "../../lib/canvas/document";
import { PUBLIC_BASE_URL } from "../../lib/constants";
import { generateQrDataUrl } from "../../lib/qr";
import type { Invitation } from "../../lib/types";
import { useFocusTrap } from "../editor/hooks/useFocusTrap";

export default function ShareModal({
	open,
	invitation,
	onClose,
}: {
	open: boolean;
	invitation: Invitation | null;
	onClose: () => void;
}) {
	const rawId = useId();
	const titleId = `share-modal-title-${rawId.replaceAll(":", "")}`;
	const dialogRef = useRef<HTMLDivElement>(null);
	const [copied, setCopied] = useState(false);
	const [pngLoading, setPngLoading] = useState(false);
	const copyTimeoutRef = useRef<ReturnType<typeof setTimeout> | null>(null);

	useEffect(() => {
		if (!open) {
			setCopied(false);
			setPngLoading(false);
			if (copyTimeoutRef.current) {
				clearTimeout(copyTimeoutRef.current);
				copyTimeoutRef.current = null;
			}
		}
		return () => {
			if (copyTimeoutRef.current) {
				clearTimeout(copyTimeoutRef.current);
			}
		};
	}, [open]);

	useFocusTrap(dialogRef, {
		enabled: open && invitation != null,
		onEscape: onClose,
	});

	const url = useMemo(
		() => (invitation ? `${PUBLIC_BASE_URL}/invite/${invitation.slug}` : ""),
		[invitation],
	);
	const qrDataUrl = useMemo(() => (url ? generateQrDataUrl(url) : ""), [url]);

	const handleDownloadPng = useCallback(async () => {
		setPngLoading(true);
		try {
			const img = new Image();
			img.onload = () => {
				const canvas = document.createElement("canvas");
				canvas.width = 400;
				canvas.height = 400;
				const ctx = canvas.getContext("2d");
				if (!ctx) {
					setPngLoading(false);
					return;
				}
				ctx.fillStyle = "#ffffff";
				ctx.fillRect(0, 0, 400, 400);
				ctx.drawImage(img, 0, 0, 400, 400);
				const pngUrl = canvas.toDataURL("image/png");
				const link = document.createElement("a");
				link.href = pngUrl;
				link.download = `invite-${invitation?.slug ?? "qr"}.png`;
				link.click();
				setPngLoading(false);
			};
			img.onerror = () => setPngLoading(false);
			img.src = qrDataUrl;
		} catch {
			setPngLoading(false);
		}
	}, [qrDataUrl, invitation?.slug]);

	if (!open || !invitation) return null;

	const summary = summarizeInvitationContent(invitation.content);
	const coupleNames = summary.title || "Our Wedding";
	const shareText = summary.date
		? `You're invited to ${coupleNames}'s wedding on ${summary.date}.`
		: `You're invited to ${coupleNames}.`;

	const btnClass =
		"rounded-full border border-[color:var(--dm-border)] px-4 py-2 text-xs uppercase tracking-[0.2em] text-[color:var(--dm-accent-strong)] transition-colors hover:bg-[color:var(--dm-surface-muted)] hover:border-[color:var(--dm-accent-strong)] focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-[color:var(--dm-accent-strong)] focus-visible:ring-offset-2 active:scale-[0.98]";

	return (
		<div
			ref={dialogRef}
			className="dm-inline-edit"
			role="dialog"
			aria-modal="true"
			aria-labelledby={titleId}
			style={{ zIndex: 40 }}
			onPointerDown={(e) => {
				if (e.target === e.currentTarget) onClose();
			}}
		>
			<div className="dm-inline-card">
				<p
					id={titleId}
					className="text-xs uppercase tracking-[0.3em] text-[color:var(--dm-accent-strong)]"
				>
					Share Invitation
				</p>
				<p
					className="mt-2 select-all rounded-lg bg-[color:var(--dm-surface-muted)] px-3 py-2 font-mono text-xs text-[color:var(--dm-muted)] break-all"
					style={{ userSelect: "all" }}
				>
					{url}
				</p>
				<div className="mt-4 grid grid-cols-2 gap-3">
					{/* Copy Link */}
					<button
						type="button"
						className={`col-span-2 rounded-full border px-4 py-2 text-xs uppercase tracking-[0.2em] transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-[color:var(--dm-accent-strong)] focus-visible:ring-offset-2 active:scale-[0.98] ${
							copied
								? "border-[color:var(--dm-sage)] bg-[color:var(--dm-sage)] text-[color:var(--dm-ink)]"
								: "border-[color:var(--dm-border)] text-[color:var(--dm-accent-strong)] hover:bg-[color:var(--dm-surface-muted)] hover:border-[color:var(--dm-accent-strong)]"
						}`}
						onClick={async () => {
							try {
								await navigator.clipboard.writeText(url);
								if (copyTimeoutRef.current)
									clearTimeout(copyTimeoutRef.current);
								setCopied(true);
								copyTimeoutRef.current = setTimeout(() => {
									setCopied(false);
									copyTimeoutRef.current = null;
								}, 3000);
							} catch {
								window.prompt("Copy this link:", url);
							}
						}}
						aria-live="polite"
						aria-label={
							copied ? "Link copied to clipboard" : "Copy link to clipboard"
						}
					>
						{copied ? "Copied!" : "Copy Link"}
					</button>

					{/* WhatsApp */}
					<button
						type="button"
						className={btnClass}
						onClick={() =>
							window.open(
								`https://wa.me/?text=${encodeURIComponent(`${shareText} ${url}`)}`,
								"_blank",
								"noopener,noreferrer",
							)
						}
					>
						<span className="inline-flex items-center justify-center gap-2">
							<svg
								viewBox="0 0 24 24"
								className="h-4 w-4 fill-current"
								aria-hidden="true"
							>
								<path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z" />
							</svg>
							WhatsApp
						</span>
					</button>

					{/* Telegram */}
					<button
						type="button"
						className={btnClass}
						onClick={() =>
							window.open(
								`https://t.me/share/url?url=${encodeURIComponent(url)}&text=${encodeURIComponent(shareText)}`,
								"_blank",
								"noopener,noreferrer",
							)
						}
					>
						<span className="inline-flex items-center justify-center gap-2">
							<svg
								viewBox="0 0 24 24"
								className="h-4 w-4 fill-current"
								aria-hidden="true"
							>
								<path d="M11.944 0A12 12 0 000 12a12 12 0 0012 12 12 12 0 0012-12A12 12 0 0012 0zm4.962 7.224c.1-.002.321.023.465.14a.506.506 0 01.171.325c.016.093.036.306.02.472-.18 1.898-.962 6.502-1.36 8.627-.168.9-.499 1.201-.82 1.23-.696.065-1.225-.46-1.9-.902-1.056-.693-1.653-1.124-2.678-1.8-1.185-.78-.417-1.21.258-1.91.177-.184 3.247-2.977 3.307-3.23.007-.032.014-.15-.056-.212s-.174-.041-.249-.024c-.106.024-1.793 1.14-5.061 3.345-.479.33-.913.49-1.302.48-.428-.008-1.252-.241-1.865-.44-.752-.245-1.349-.374-1.297-.789.027-.216.325-.437.893-.663 3.498-1.524 5.83-2.529 6.998-3.014 3.332-1.386 4.025-1.627 4.476-1.635z" />
							</svg>
							Telegram
						</span>
					</button>

					{/* Email */}
					<button
						type="button"
						className={btnClass}
						onClick={() => {
							window.location.href = `mailto:?subject=${encodeURIComponent(`You're invited: ${coupleNames}`)}&body=${encodeURIComponent(`${shareText}\n\n${url}`)}`;
						}}
					>
						<span className="inline-flex items-center justify-center gap-2">
							<svg
								viewBox="0 0 24 24"
								className="h-4 w-4 fill-none stroke-current"
								strokeWidth="2"
								strokeLinecap="round"
								strokeLinejoin="round"
								aria-hidden="true"
							>
								<rect x="2" y="4" width="20" height="16" rx="2" />
								<path d="m22 7-8.97 5.7a1.94 1.94 0 0 1-2.06 0L2 7" />
							</svg>
							Email
						</span>
					</button>

					{/* Web Share API (mobile native sharing) */}
					{typeof navigator !== "undefined" && "share" in navigator && (
						<button
							type="button"
							className={btnClass}
							onClick={() =>
								navigator
									.share({
										title: coupleNames,
										text: shareText,
										url: url,
									})
									.catch(() => {})
							}
						>
							<span className="inline-flex items-center justify-center gap-2">
								<svg
									viewBox="0 0 24 24"
									className="h-4 w-4 fill-none stroke-current"
									strokeWidth="2"
									strokeLinecap="round"
									strokeLinejoin="round"
									aria-hidden="true"
								>
									<circle cx="18" cy="5" r="3" />
									<circle cx="6" cy="12" r="3" />
									<circle cx="18" cy="19" r="3" />
									<line x1="8.59" y1="13.51" x2="15.42" y2="17.49" />
									<line x1="15.41" y1="6.51" x2="8.59" y2="10.49" />
								</svg>
								More...
							</span>
						</button>
					)}
				</div>
				<div className="mt-5 flex flex-col items-center gap-3">
					<p className="text-xs uppercase tracking-[0.2em] text-[color:var(--dm-muted)]">
						Scan QR Code
					</p>
					<img
						src={qrDataUrl}
						alt="Invitation QR code"
						width={160}
						height={160}
						className="h-40 w-40 rounded-2xl"
					/>
					<div className="flex gap-2">
						<a
							href={qrDataUrl}
							download={`invite-${invitation.slug}.svg`}
							className={`text-center ${btnClass}`}
						>
							SVG
						</a>
						<button
							type="button"
							className={`text-center ${btnClass}`}
							onClick={handleDownloadPng}
							disabled={pngLoading}
						>
							{pngLoading ? "Saving..." : "PNG"}
						</button>
					</div>
				</div>
				<button
					type="button"
					className="mt-4 w-full min-h-11 rounded-full bg-[color:var(--dm-accent-strong)] px-4 py-2 text-xs uppercase tracking-[0.2em] text-[color:var(--dm-on-accent)]"
					onClick={onClose}
				>
					Close
				</button>
			</div>
		</div>
	);
}
